

function H = datafit_2_Hess(x)
    y = [1.5; 2.25; 2.625];
    H = [0 0; 0 0];
    w = x(1);
    z = x(2);
    for i = 1:3
        H(1,1) = H(1,1) + 2*(1-z^i)^2;
        H(1,2) = H(1,2) + 2*i*z^(i-1)*(y(i) - 2*w*(1-z^i));
        H(2,2) = H(2,2) + 2*(i*w*z^(i-1))^2 + 2*i*(i-1)*w*z^(i-2)*(y(i)-w*(1-z^i));
    end

    H(2,1) = H(1,2);


end